package Otro;

/**
 *
 * @author José Sánchez
 */
public class Rectangulo {
    
    double l1, l2;
    
    public Rectangulo(){
        
    }
    
    public Rectangulo(double l1, double l2){
        this.l1 = l1;
        this.l2 = l2;
    }

    public double getL1() {
        return l1;
    }

    public double getL2() {
        return l2;
    }
    
    
    
}
